#include <stdio.h>

int main() {
    int i, j, k;
    for (i = 0; i < 7; i++) {
        for (j = 0; j < i + 1; j++) {
            printf("*");
        }
        for (k = i; k < 7; k++) {
            printf(" ");
        }
        for (k = 7; k < 14 - i - 1; k++) {
            printf(" ");
        }
        for (j = 14 - i - 1; j < 14; j++) {
            printf("*");
        }
        printf("\n");
    }
}